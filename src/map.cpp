Map::Map() {
    dirt = Texture::loadTexture("../assets/dirt.png");
    grass = Texture::loadTexture("../assets/grass.png");
    water = Texture::loadTexture("../assets/water.png");

    loadMap(level1);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
    dest.x = dest.y = 0;
}

Map::~Map() {

}

void Map::loadMap(int arr[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::drawMap() {
    int type = 0;

    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            type = map[row][col];

            dest.x = col * 32;
            dest.y = row * 32;

            switch (type) {
                case 0:
                    Texture::draw(water, src, dest);
                    break;
                case 1:
                    Texture::draw(grass, src, dest);
                    break;
                case 2:
                    Texture::draw(dirt, src, dest);
                    break;
                default:
                    break;
            }
        }
    }
}